class bcolors:
    # End coloring.
    ENDC = '\033[0m'

    # Foreground.
    BLUE = '\033[94m'
    BROWN = '\033[1m'
    GREEN = '\033[92m'
    HEADER = '\033[95m'
    PURPLE = '\033[35m'
    RED = '\033[91m'
    YELLOW = '\033[93m'

    # Text decoration.
    UNDERLINE = '\033[4m'

    # Background.
    ON_WHITE = '\033[47m'
    ON_BLUE= '\033[44m'

